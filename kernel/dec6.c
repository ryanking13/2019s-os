int is_neg_dec6(struct decimal_6 a)
{
    return a.integer<0 || (a.integer==0 && a.fractional<0);
}

struct decimal_6 neg_dec6(struct decimal_6 a)
{
    struct decimal_6 result = a;
    if(result.integer==0)
        result.fractional*=(-1);
    else
        result.integer*=(-1);
    return result;
}

struct decimal_6 mult_dec6(struct decimal_6 a, struct decimal_6 b)
{
    struct decimal_6 result;
    struct decimal_6 a_ = a;
    struct decimal_6 b_ = b;
    int flag =0,n;
    s64 f,t;
    if(is_neg_dec6(a_))
    {
        a_ = neg_dec6(a_);
        flag = 1-flag;
    }
    if(is_neg_dec6(b_))
    {
        b_ = neg_dec6(b_);
        flag = 1-flag;
    }
    
    t = (s64)a_.integer * b_.fractional
                + (s64)b_.integer * a_.fractional;

    n = a_.integer*b_.integer;
    f = t + div64_s64((s64)a_.fractional * b_.fractional,(s64)1000000);
    n+=div_s64_rem(f,(s32)1000000,&result.fractional);

    result.integer=n;

    if(flag)
        result = neg_dec6(result);
    return result;
}

struct decimal_6 sum_dec6(struct decimal_6 a, struct decimal_6 b)
{
    struct decimal_6 result;
    s32 fractional;
    int flag = 0;
    s64 a_ = (s64)a.integer * 1000000
                 + a.fractional *(1-2*(a.integer<0));
    s64 b_ = (s64)b.integer * 1000000
                 + b.fractional *(1-2*(b.integer<0));

    s64 s = a_+b_;
    if(s<0)
    {
        s = -s;
        flag = 1;
    }
    result.integer = (int)(div_s64_rem(s,(s32)1000000,&fractional));
    result.fractional = (int)fractional;

    if(flag)
        result = neg_dec6(result);
    return result;
}

struct decimal_6 div_int_dec6(struct decimal_6 a, int n)
{
    struct decimal_6 result;
    s32 fractional;
    int flag = 0;
    s64 a_ = (s64)a.integer * 1000000
                 + a.fractional *(1-2*(a.integer<0));
    s64 d = div64_s64(a_,(s64)n);
    if(d<0)
    {
        d = -d;
        flag = 1;
    }

    result.integer = (int)(div_s64_rem(d,(s32)1000000,&fractional));
    result.fractional = (int)fractional;

    if(flag)
        result = neg_dec6(result);
    return result;
}

