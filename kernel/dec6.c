struct decimal_6 sqrt_precision_dec6(struct decimal_6 number)
{
    struct decimal_6 x = number;
    struct decimal_6 y = {0,0};
    struct decimal_6 s = x;
    struct decimal_6 mid, mid_p;
    if (x.integer==0)
    {
        if (x.fractional ==0)
            return x;
        x.integer = 1;
        x.fractional = 0;
    }

    while((s64)s.integer*1000000 + s.fractional > 1)
    {
        mid = div_int_dec6(sum_dec6(x,y),2);
        mid_p = mult_dec6(mid,mid);
        if(mid_p.integer==number.integer && mid_p.fractional==number.fractional)
            return mid;
        if (mid_p.integer>number.integer ||
            (mid_p.integer==number.integer && mid_p.fractional>number.fractional))
            x = mid;
        else
            y = mid;
        s = sum_dec6(x,neg_dec6(y));
    }
    return x;
}

struct decimal_6 theta_between_dec6(struct decimal_6 a1, struct decimal_6 a2, struct decimal_6 b1, struct decimal_6 b2)
{
    struct decimal_6 t1, t2;
    struct decimal_6 c = {25,0};
    struct decimal_6 c2 = {2,0};
    struct decimal_6 t = mult_dec6(
                mult_dec6(
                    cos_precision_dec6(
                        sum_dec6(a1, neg_dec6(b1)))
                    , cos_precision_dec6(a2))
                , cos_precision_dec6(b2));
    t = sum_dec6(
            t
            , mult_dec6(
                sin_precision_dec6(a2)
                , sin_precision_dec6(b2)));
    if (t.integer!=1 && t.fractional<850000)
        return acos_precision_dec6(t);
    else
    {
        t1 = mult_dec6(
                sin_precision_dec6(
                    div_int_dec6(
                        sum_dec6(a2, neg_dec6(b2))
                        , 2))
                , c);
        t1 = mult_dec6(t1,t1);
        t2 = mult_dec6(
                sin_precision_dec6(
                    div_int_dec6(
                        sum_dec6(a1,neg_dec6(b1))
                        , 2))
                , c);
        t2 = mult_dec6(t2,t2);
        t2 = mult_dec6(
                t2
                , mult_dec6(
                    cos_precision_dec6(a2)
                    , cos_precision_dec6(b2)));
        t = mult_dec6(
                asin_precision_dec6(
                    div_int_dec6(
                        sqrt_precision_dec6(
                            sum_dec6(t1,t2))
                        , 25))
                , c2);
        return t;
    }
}

struct decimal_6 get_distance_dec6(struct decimal_6 a1, struct decimal_6 a2, struct decimal_6 b1, struct decimal_6 b2)
{
    struct decimal_6 radii = {6371,0};
    return mult_dec6(radii, theta_between_dec6(a1,a2,b1,b2));
}
